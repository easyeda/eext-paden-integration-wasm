/**
 * Build script: Embed Python service files into service-check.html
 *
 * This script reads all Python files from paden-service/ and embeds them
 * as <script type="text/plain"> tags in ui/service-check.html.
 *
 * Run with: node scripts/embed-python-files.js
 */

const fs = require('fs');
const path = require('path');

// Configuration
const CONFIG = {
  htmlPath: path.join(__dirname, '..', 'ui', 'service-check.html'),
  serviceDir: path.join(__dirname, '..', 'paden-service'),
  files: [
    'calculation.py',
    'main.py',
    'mesh_pure.py',
    'problem.py',
    'solver_enhanced.py',
    'start-paden-windows.bat',
    'start-paden-linux.sh',
  ],
  // Patterns to exclude (glob-style)
  exclude: [
    '*.pyc',
    '__pycache__',
    '*.pyo',
  ],
};

/**
 * Escape HTML special characters (but not inside <script type="text/plain">)
 * In <script type="text/plain">, only </script> needs to be escaped
 */
function escapeScriptContent(text) {
  // Only need to escape </script> to prevent early tag closure
  return text.replace(/<\/script>/gi, '<\\/script>');
}

/**
 * Read a file and return its content
 */
function readFileContent(filePath) {
  try {
    return fs.readFileSync(filePath, 'utf8');
  } catch (e) {
    throw new Error(`Failed to read ${filePath}: ${e.message}`);
  }
}

/**
 * Generate the script tag for a file
 */
function generateScriptTag(fileName, content) {
  const id = 'file-' + fileName.replace(/\./g, '-');
  const escapedContent = escapeScriptContent(content);
  return `<script type="text/plain" id="${id}">${escapedContent}</script>`;
}

/**
 * Main function
 */
function main() {
  console.log('📦 Embedding Python service files into service-check.html...\n');

  // Read HTML file
  let html;
  try {
    html = fs.readFileSync(CONFIG.htmlPath, 'utf8');
  } catch (e) {
    console.error(`❌ Failed to read HTML file: ${CONFIG.htmlPath}`);
    console.error(`   ${e.message}`);
    process.exit(1);
  }

  // Remove existing embedded script tags
  const originalLength = html.length;
  html = html.replace(/<script type="text\/plain" id="file-[^"]*">[\s\S]*?<\/script>/g, '');
  const removedCount = originalLength - html.length;

  if (removedCount > 0) {
    console.log(`   Removed ${removedCount} characters of old embedded content`);
  }

  // Generate new script tags
  const scriptTags = [];
  const totalSize = { bytes: 0, files: 0 };

  for (const fileName of CONFIG.files) {
    const filePath = path.join(CONFIG.serviceDir, fileName);

    // Check if file exists
    if (!fs.existsSync(filePath)) {
      console.warn(`   ⚠️  File not found: ${fileName} (skipping)`);
      continue;
    }

    // Check if file matches exclude patterns
    const shouldExclude = CONFIG.exclude.some(pattern => {
      const regex = new RegExp(
        '^' + pattern.replace(/\*/g, '.*').replace(/\?/g, '.') + '$'
      );
      return regex.test(fileName);
    });

    if (shouldExclude) {
      console.log(`   ⊘ ${fileName} (excluded)`);
      continue;
    }

    // Read and embed
    const content = readFileContent(filePath);
    scriptTags.push(generateScriptTag(fileName, content));

    totalSize.bytes += content.length;
    totalSize.files++;

    const sizeKB = (content.length / 1024).toFixed(1);
    console.log(`   ✓ ${fileName} (${sizeKB} KB)`);
  }

  if (totalSize.files === 0) {
    console.warn('\n⚠️  No files were embedded. Check your configuration.');
    return;
  }

  // Find the insertion point (before </body> or at the end of file)
  const bodyEndIndex = html.lastIndexOf('</body>');
  const insertIndex = bodyEndIndex !== -1 ? bodyEndIndex : html.length;

  // Insert script tags
  const newHtml = html.slice(0, insertIndex) +
    '\n\n<!-- Embedded Python service files (auto-generated by scripts/embed-python-files.js) -->\n' +
    scriptTags.join('\n') +
    (bodyEndIndex !== -1 ? '\n\n' + html.slice(bodyEndIndex) : '');

  // Write back
  try {
    fs.writeFileSync(CONFIG.htmlPath, newHtml, 'utf8');
  } catch (e) {
    console.error(`\n❌ Failed to write HTML file: ${CONFIG.htmlPath}`);
    console.error(`   ${e.message}`);
    process.exit(1);
  }

  const totalSizeKB = (totalSize.bytes / 1024).toFixed(1);
  console.log(`\n✅ Successfully embedded ${totalSize.files} files (${totalSizeKB} KB)`);
  console.log(`   HTML size: ${(newHtml.length / 1024).toFixed(1)} KB\n`);
}

// Run
main();
